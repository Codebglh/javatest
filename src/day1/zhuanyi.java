public class zhuanyi {
    //制符标
    public static void main(String[] args) {
        //\t:一个制表符，实现对齐功能
        System.out.println("1\t2\t3");
        //\n:一个换行符
        System.out.println("1\n2\n3");
        //\\:一个\
        System.out.println("1\\2\\3");
        //\"：一个“
        System.out.println("1\"2\"3");
        //\'：一个‘
        System.out.println("1\'2\'3");
        //  \r：一个回车
        System.out.println("123\r45\r6");

        
    }

}
